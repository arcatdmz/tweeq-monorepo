#!/usr/bin/env node
import {readFileSync, writeFileSync} from 'node:fs'
import {dirname, join, relative, resolve} from 'node:path'
import {fileURLToPath} from 'node:url'

import ts from 'typescript'

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..')
const outputPath = join(root, 'docs', 'api-exports.md')
const packageDirs = ['core', 'dom', 'styles', 'react', 'vue']

function exportEntries(manifest) {
	return Object.entries(manifest.exports ?? {}).map(([subpath, value]) => {
		const conditions = typeof value === 'string' ? {default: value} : value
		return {subpath, conditions}
	})
}

function symbolsFor(typesPath) {
	const program = ts.createProgram([typesPath], {
		module: ts.ModuleKind.ESNext,
		moduleResolution: ts.ModuleResolutionKind.Bundler,
		skipLibCheck: true,
		target: ts.ScriptTarget.ESNext,
	})
	const source = program.getSourceFile(typesPath)
	if (!source) throw new Error(`Cannot load declaration entry ${typesPath}`)
	const moduleSymbol = program.getTypeChecker().getSymbolAtLocation(source)
	if (!moduleSymbol) throw new Error(`Cannot inspect declaration entry ${typesPath}`)
	return program.getTypeChecker().getExportsOfModule(moduleSymbol)
		.map(symbol => symbol.name)
		.filter(name => name !== 'default')
		.sort((a, b) => a.localeCompare(b))
}

const lines = [
	'# Public package exports',
	'',
	'<!-- Generated by scripts/generate-api-reference.mjs. Do not edit by hand. -->',
	'',
	'This inventory is generated from each package export map and its built',
	'declaration entry. `pnpm docs:check` fails when this file is stale.',
	'',
]

for (const dir of packageDirs) {
	const packageDir = join(root, 'packages', dir)
	const manifest = JSON.parse(readFileSync(join(packageDir, 'package.json'), 'utf8'))
	lines.push(`## \`${manifest.name}\``, '')
	for (const {subpath, conditions} of exportEntries(manifest)) {
		const publicPath = subpath === '.' ? manifest.name : `${manifest.name}/${subpath.slice(2)}`
		lines.push(`### \`${publicPath}\``, '')
		const targets = Object.entries(conditions)
			.map(([condition, target]) => `- ${condition}: \`${target}\``)
		lines.push(...targets, '')
		if (conditions.types && !conditions.types.includes('*')) {
			const typesPath = resolve(packageDir, conditions.types)
			const symbols = symbolsFor(typesPath)
			lines.push(symbols.length ? symbols.map(name => `\`${name}\``).join(', ') : '_No named exports._', '')
		}
	}
}

const generated = `${lines.join('\n').trim()}\n`
if (process.argv.includes('--check')) {
	const current = readFileSync(outputPath, 'utf8')
	if (current !== generated) {
		throw new Error(`${relative(root, outputPath)} is stale; run pnpm docs:generate`)
	}
} else {
	writeFileSync(outputPath, generated)
	console.log(`generated ${relative(root, outputPath)}`)
}
