import fs from 'node:fs'
import path from 'node:path'
import {fileURLToPath} from 'node:url'

// Extract the VuePress theme's SFC-authored chrome styles for the React docs.
// Run `pnpm --filter @tweeq/docs extract:theme-styles` after upgrading the
// theme. Paths are resolved from this script so the command works from any
// directory.
const stylesDir = path.dirname(fileURLToPath(import.meta.url))
const pkgRoot = path.resolve(
	stylesDir,
	'../node_modules/@vuepress/theme-default/lib/client'
)
const outDir = path.join(stylesDir, 'theme-sfc')
fs.rmSync(outDir, {recursive: true, force: true})
fs.mkdirSync(outDir, {recursive: true})

const files = ['components', 'layouts'].flatMap(dir =>
	fs
		.readdirSync(path.join(pkgRoot, dir), {recursive: true})
		.filter(file => String(file).endsWith('.vue'))
		.map(file => path.join(pkgRoot, dir, String(file)))
)

// Lazy-loaded route components whose styles never inject on normal pages
// in a real VuePress app (NotFound styles the whole .vp-theme-container
// down to a 740px column — only correct on 404 pages).
const excluded = new Set(['NotFound.vue'])

const partials = []
for (const file of files.sort()) {
	if (excluded.has(path.basename(file))) continue
	const src = fs.readFileSync(file, 'utf8')
	const blocks = [...src.matchAll(/<style[^>]*>([\s\S]*?)<\/style>/g)]
	if (!blocks.length) continue
	const name = path.basename(file, '.vue')
	let css = blocks.map(block => block[1]).join('\n')
	// SFC-relative imports -> package specifiers (vite's sass importer
	// resolves bare specifiers through node_modules)
	css = css.replaceAll(
		/@use\s+'(\.\.\/)+styles\//g,
		"@use '../../node_modules/@vuepress/theme-default/lib/client/styles/"
	)
	fs.writeFileSync(`${outDir}/_${name}.scss`, css)
	partials.push(name)
}
fs.writeFileSync(
	`${outDir}/index.scss`,
	'// GENERATED by ../extract-theme-styles.mjs — do not edit by hand.\n' +
		partials.map(name => `@use './${name}';`).join('\n') +
		'\n'
)
process.stdout.write(`extracted ${partials.length} partials\n`)
